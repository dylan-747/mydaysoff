export function getShowcaseEvents() {
  return RAW.map((e, i) => {
    const start = addDays(e.day);
    const verification = e.trust === "community" ? "community-submitted" : "feed-listing";
    return {
      id: `showcase_${i}_${e.city.toLowerCase().replace(/\s+/g, "")}`,
      name: e.name,
      start_date: start,
      end_date: start,
      time: e.time,
      category: e.category,
      cost: e.cost,
      venue: e.venue,
      city: e.city,
      url: e.url,
      source_event_url: e.url,
      source_feed_url: "",
      what3words: W3W[i % W3W.length],
      lat: near(e.lat, i + 1),
      lng: near(e.lng, i + 7),
      summary: e.summary,
      vibe: e.vibe,
      indoor: e.indoor,
      activity_level: e.activity_level || "low",
      audience: ["all-ages"],
      accessibility: ["step-free"],
      source: "showcase",
      source_trust: e.trust === "community" ? "community" : "official",
      verification_status: verification,
      popularity: e.likes,
      last_seen_at: new Date().toISOString(),
    };
  });
}

export default getShowcaseEvents;
