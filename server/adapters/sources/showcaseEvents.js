function addDays(days) {
  const d = new Date();
  d.setDate(d.getDate() + days);
  return d.toISOString().slice(0, 10);
}

// deterministic jitter so co-located events don't stack on one pixel
function near(base, seed) {
  const j = ((seed * 9301 + 49297) % 233280) / 233280;
  return base + (j - 0.5) * 0.04;
}

